<h3>Periodo de Inscripción</h3>
<p>El periodo de inscripción comenzará a las 9 horas del día <b>1 de Marzo</b> y finalizará
a las 23:59 horas del día <b>30 de Abril</b> de 2015.</p>
<p>El Congreso comenzará a las 9 h del día 4 de Junio hasta las 20 horas del día 5 de Junio 
de 2015 acorde con el Corpus Christi.</p>

<h3>Cómo Inscribirse</h3>
<p><b><u>Muy Importante y Fundamental</u></b>: Antes de realizar la inscripción debe <b>AÑADIR
a sus contactos</b> el correo electrónico del congreso para asegurar que recibe las claves de
acceso, así como la comunicación con la Secretaría del Congreso: <a>ceiie@congresoinformatica.com</a></p>    
<p>Se podrá realizar la inscripción en el apartado correspondiente efectuando el pago con tarjeta 
de crédito en página segura (https) o mediante transferencia bancaria. El precio de la entrada
varía dependiendo de la categoría de cada persona: <b>profesor</b>, <b>alumno</b> o <b>profesional</b>.</p>

