<h1><img class="logo" src="images/ic_action_present.png"/> Presentación</h1>
<p>En 2015 se celebrará la I edición del Congreso de Estudiantes de Ingeniería Informática en 
España(CEIIE). Estas jornadas, organizadas por la Escuela de Ingeniería Informática de la 
Universidad de Informática, nacieron como foro de intercambio de ideas en el área de la enseñanza
universitaria en informática.</p>

<p>El objetivo del CEIIE es promover el contacto, el intercambio y discusión de conocimientos y 
experiencias entre profesores universitarios de informática y grupos de investigación, debatir 
sobre el contenido de los programas y los métodos pedagógicos empleados, y presentar temas y 
enfoques innovadores que permitan mejorar el aprendizaje de la informática en las universidades.</p>

