<h3>Granada</h3>
<p class="margen"><b>Granada</b> se encuentra situada en el sudeste de la península Ibérica,
a los pies de Sierra Nevada y a pocos kilómetros del mar Mediterráneo.</p>
<p>Se puede acceder por <b>carretera</b> (automóvil y autobús), <b>tren</b>, <b>avión</b>
(aeropuertos de Granada, Málaga y Madrid) y <b>barco</b> (por el puerto de Motril).</p>        

